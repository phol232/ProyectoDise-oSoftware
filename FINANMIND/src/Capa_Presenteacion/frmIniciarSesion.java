    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Obtener los datos del formulario
        String idUsuario = txtUsuario.getText();
        String contraseña = new String(txtContraseña.getPassword());

        // Validar credenciales en la capa de negocio
        neg_usuario negocio = new neg_usuario();
        boolean valido = negocio.validarLogin(idUsuario, contraseña);

        if (valido) {
            JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso.");
            
            // Abrir el formulario principal (ejemplo: Dashboard)
            frmCliente dashboard = new frmCliente(idUsuario); 
            dashboard.setVisible(true);
            this.dispose(); // Cierra el formulario de login
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos.");
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

